#pragma once

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////

#include <memory>
#include <unordered_map>

namespace ls6502
{
//////////////////////////////////////////////////////////////
///
/// @struct Node
///
/// @brief A structure representing a node in a trie.
///
//////////////////////////////////////////////////////////////
struct Node
{
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Indicates whether the node is a leaf.
    ///
    //////////////////////////////////////////////////////////////
    bool isLeaf;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief A map of child nodes indexed by characters.
    ///
    //////////////////////////////////////////////////////////////
    std::unordered_map<char, std::shared_ptr<Node>> children;
};
} // namespace ls6502
